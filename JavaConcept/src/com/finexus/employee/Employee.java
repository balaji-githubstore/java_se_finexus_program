package com.finexus.employee;

public class Employee {
	public int empId;
	public String empName;
	public double empSalary;
	public static String companyName;
		
	public void displayEmployeeDetails()
	{
		System.out.println("Employee Id: "+empId);
		System.out.println("Employee Name: "+empName);
		System.out.println("Employee Salary: "+empSalary);
		System.out.println("Company Name: "+Employee.companyName);
		System.out.println("------------------------------------");
	}
	
	public static void displayAllEmployeeDetails(Employee[] empArr)
	{
		for(int i=0;i<empArr.length;i++)
		{
			System.out.println("Employee Id: "+empArr[i].empId);
			System.out.println("Employee Name: "+empArr[i].empName);
			System.out.println("Employee Salary: "+empArr[i].empSalary);
			System.out.println("Company Name: "+Employee.companyName);
			System.out.println("------------------------------------");
		}
	}
}
